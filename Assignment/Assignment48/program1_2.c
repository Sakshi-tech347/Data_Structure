int main()
{

    PNODE head = NULL;


    InsertFirst(&head ,51);
    InsertFirst(&head ,24);
    InsertFirst(&head ,11);
    InsertFirst(&head ,12);
    InsertFirst(&head ,11);



    printf("original :\n");
    Display(head);


    ReplaceOdd(&head);
     

    printf("odd replace with 1:\n");

    Display(head);





    return 0;
}



