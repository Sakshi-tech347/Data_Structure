void ReplaceOdd(PPNODE first)
{
    PNODE temp = NULL;
    temp = *first;
       while ( temp != NULL)
    {
        
        if(temp ->data  % 2  != 0)
        {
             temp->data = 1;
            
        }
           temp =temp->next;
 
        
    }

     
    
      
}

