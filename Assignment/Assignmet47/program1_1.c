void DisplayReverse(PNODE first)
{
     int iCnt =0 ;
     int i =0 , j =0;
     PNODE temp = NULL;

     temp = first;

     while (temp != NULL)
     {
        iCnt++;
        temp = temp->next;
     }

     for(i = iCnt; i >= 1 ;i --)
     {
            temp = first;
            for(j = 1; j< i;j++ )
            {
                temp = temp->next;
            }
            printf("|%d| ",temp ->data);
     }
      
}

