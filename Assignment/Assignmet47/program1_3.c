struct node
{
    int data;
    struct node * next;
};

typedef struct node NODE;
typedef struct node * PNODE;
typedef struct node ** PPNODE;


void InsertFirst(PPNODE first , int no)
{
    PNODE newn = NULL;
    newn = (PNODE)malloc(sizeof(NODE));

    newn ->data = no;
    newn->next = NULL;

    if(*first == NULL)
    {
        *first = newn;
        
    }
    else 
    {
        newn->next = *first;
        *first = newn ;
    }


}

 


